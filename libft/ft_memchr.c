#include "libft.h"

char	*ft_memchr(const char *s, int c, size_t n)
{
	size_t	i;

	i = -1;
	while (++i != n)
	{
		if (s[i] == (char)c)
			return ((char *)&s[i]);
	}
	return (NULL);
}
